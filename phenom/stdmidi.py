def m0(f):
    return f

def m1(f):
    return f + 1.0

def m2(f):
    return 4.0 * f + 1.0

def m3(f):
    return 5.0 * f

def m4(f):
    return 2.0 * 3.14159 * f

def m0_inv(f):
    return f

def m1_inv(f):
    return f - 1.0

def m2_inv(f):
    return (f - 1.0) / 4.0

def m3_inv(f):
    return f / 5.0

def m4_inv(f):
    return f / (2.0 * 3.14159)
